export const getAllProtocols = () => {
  const all: typeof PROTOCOLS.ethereum = []
  Object.values(PROTOCOLS).forEach((protocols) => {
    all.push(...protocols)
  })
  return all.sort((a, b) => b.tvl - a.tvl)
}

export const getTopAPY = (limit: number = 10) => {
  return getAllProtocols().sort((a, b) => b.apy - a.apy).slice(0, limit)
}

export const getTopTVL = (limit: number = 10) => {
  return getAllProtocols().sort((a, b) => b.tvl - a.tvl).slice(0, limit)
}
